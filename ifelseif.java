public class ifelseif {
    public static void main(String[] args){
        int x = 4;
        int y = 10;
        int z = 1;
        if(x>y && x>z)
            System.out.println("x is greater");
        else if(y>x && y>z)
            System.out.println("y is greater");
        else
            System.out.println("z is greater");
    }
    
}
